  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            zhaopinbianhao : false,
            gangweimingcheng : false,
            qiyezhanghao : false,
            qiyemingcheng : false,
            gongzuoleixing : false,
            lianxidianhua : false,
            qiyeyouxiang : false,
            xuehao : false,
            xingming : false,
            shouji : false,
            jianliwenjian : false,
            toujianshijian : false,
            sfsh : false,
            shhf : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          zhaopinbianhao: this.getUUID(),
          gangweimingcheng: '',
          qiyezhanghao: '',
          qiyemingcheng: '',
          gongzuoleixing: '',
          lianxidianhua: '',
          qiyeyouxiang: '',
          xuehao: '',
          xingming: '',
          shouji: '',
          jianliwenjian: '',
          toujianshijian: '',
